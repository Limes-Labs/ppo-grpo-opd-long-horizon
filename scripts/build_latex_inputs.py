def build_closed_loop_table(default: dict[str, Any], low_coverage: dict[str, Any]) -> str:
    labels = {
        "group_total": "Group",
        "group_length_norm": "Len-group",
        "critic_td": "Critic TD",
        "coverage_gated": "Cov-gated",
    }
    settings = [
        ("default", default),
        ("low cov.", low_coverage),
    ]
    lines = [
        "% Generated by scripts/build_latex_inputs.py; do not edit by hand.",
        r"\begin{tabular}{@{}llrrrrr@{}}",
        r"\toprule",
        r"Setting & Method & Init $R$ & Final $R$ & $\Delta R$ & Success & Critic frac. \\",
        r"\midrule",
    ]
    for setting, result in settings:
        for row_data in result["method_summaries"]:
            row = " & ".join(
                [
                    latex_escape(setting),
                    latex_escape(labels.get(row_data["method"], row_data["method"])),
                    fmt(row_data["initial_return"]),
                    fmt(row_data["final_return"]),
                    fmt(row_data["return_improvement"]),
                    fmt(row_data["final_success"]),
                    fmt(row_data["final_critic_fraction"]),
                ]
            )
            lines.append(row + r" \\")
        if setting != settings[-1][0]:
            lines.append(r"\addlinespace[2pt]")
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    return "\n".join(lines)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--matrix-json",
        type=Path,
        default=Path("results/deep_matrix_20seed.json"),
    )
    parser.add_argument(
        "--variance-grid-json",
        type=Path,
        default=Path("results/variance_credit_grid_seed17.json"),
    )
    parser.add_argument(
        "--length-audit-json",
        type=Path,
        default=Path("results/length_imbalance_audit_seedset.json"),
    )
    parser.add_argument(
        "--token-cost-json",
        type=Path,
        default=Path("results/token_cost_sensitivity_20seed.json"),
    )
    parser.add_argument(
        "--closed-loop-json",
        type=Path,
        default=Path("results/closed_loop_credit_training_10seed.json"),
    )
    parser.add_argument(
        "--closed-loop-low-coverage-json",
        type=Path,
        default=Path("results/closed_loop_credit_training_low_coverage_10seed.json"),
    )
    parser.add_argument("--output-dir", type=Path, default=Path("paper/generated"))
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    result = json.loads(args.matrix_json.read_text())
    variance_grid = json.loads(args.variance_grid_json.read_text())
    length_audit = json.loads(args.length_audit_json.read_text())
    token_cost = json.loads(args.token_cost_json.read_text())
    closed_loop = json.loads(args.closed_loop_json.read_text())
    closed_loop_low_coverage = json.loads(args.closed_loop_low_coverage_json.read_text())
    args.output_dir.mkdir(parents=True, exist_ok=True)

    macros = args.output_dir / "result_macros.tex"
    table = args.output_dir / "deep_matrix_table.tex"
    axis_summary = args.output_dir / "axis_summary_table.tex"
    full_case = args.output_dir / "full_case_table.tex"
    raw_seed = args.output_dir / "raw_seed_table.tex"
    raw_error = args.output_dir / "raw_error_table.tex"
    variance_credit = args.output_dir / "variance_credit_table.tex"
    length_imbalance = args.output_dir / "length_imbalance_table.tex"
    token_cost_table = args.output_dir / "token_cost_table.tex"
    closed_loop_table = args.output_dir / "closed_loop_training_table.tex"
    macros.write_text(build_macros(result) + "\n")
    table.write_text(build_table(result) + "\n")
    axis_summary.write_text(build_axis_summary_table(result) + "\n")
    full_case.write_text(build_full_case_table(result) + "\n")
    raw_seed.write_text(build_raw_seed_table(result) + "\n")
    raw_error.write_text(build_error_seed_table(result) + "\n")
    variance_credit.write_text(build_variance_credit_table(variance_grid) + "\n")
    length_imbalance.write_text(build_length_imbalance_table(length_audit) + "\n")
    token_cost_table.write_text(build_token_cost_table(token_cost) + "\n")
    closed_loop_table.write_text(
        build_closed_loop_table(closed_loop, closed_loop_low_coverage) + "\n"
    )

    print(f"wrote {macros}")
    print(f"wrote {table}")
    print(f"wrote {axis_summary}")
    print(f"wrote {full_case}")
    print(f"wrote {raw_seed}")
    print(f"wrote {raw_error}")
    print(f"wrote {variance_credit}")
    print(f"wrote {length_imbalance}")
    print(f"wrote {token_cost_table}")
    print(f"wrote {closed_loop_table}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
