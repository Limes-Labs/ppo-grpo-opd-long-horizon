"""Generate LaTeX result inputs from canonical experiment JSON."""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from statistics import fmean
from typing import Any


SELECTED_CASES = [
    "horizon_04_baseline",
    "horizon_16_long_wait",
    "critic_budget_002_full",
    "critic_budget_128_full",
    "observability_blind",
    "blind_undercovered_counterexample",
    "sparse_hard_group08",
]


def latex_escape(value: str) -> str:
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(char, char) for char in value)


def fmt(value: float) -> str:
    return f"{value:.3f}"


def display_case_name(case_name: str) -> str:
    labels = {
        "blind_undercovered_counterexample": "blind undercov.",
        "observability_full": "obs full",
        "observability_coarse": "obs coarse",
        "observability_blind": "obs blind",
    }
    normalized = labels.get(case_name, case_name)
    normalized = normalized.replace("critic_budget_", "budget_")
    normalized = normalized.replace("group_size_", "group_")
    normalized = normalized.replace("sparse_hard_", "sparse_")
    return latex_escape(normalized.replace("_", " "))


def display_axis_name(axis: str) -> str:
    labels = {
        "critic_budget": "budget",
        "group_size": "group",
        "sparse_reward": "sparse",
        "counterexample": "ctr.",
        "observability": "obs",
    }
    return latex_escape(labels.get(axis, axis.replace("_", " ")))


def display_ci_read(evidence: str) -> str:
    labels = {
        "critic_clear": "critic",
        "group_clear": "group",
        "near_tie": "near tie",
    }
    return latex_escape(labels[evidence])


def macro_line(name: str, value: str | int | float) -> str:
    return rf"\newcommand{{\{name}}}{{{value}}}"


def build_macros(result: dict[str, Any]) -> str:
    overall = result["overall"]
    lines = [
        "% Generated by scripts/build_latex_inputs.py; do not edit by hand.",
        macro_line("DeepSeedCount", result["seed_count"]),
        macro_line("DeepCaseCount", result["case_count"]),
        macro_line("DeepMeanCriticWins", overall["critic_wins_by_mean_correlation"]),
        macro_line("DeepMeanGroupWins", overall["group_wins_by_mean_correlation"]),
        macro_line("DeepClearCriticCases", overall["clear_critic_cases_by_ci95"]),
        macro_line("DeepNearTieCases", overall["near_tie_cases_by_ci95"]),
        macro_line("DeepClearGroupCases", overall["clear_group_cases_by_ci95"]),
        macro_line("DeepMeanDeltaCorrelation", fmt(overall["mean_delta_correlation"])),
        "",
    ]
    return "\n".join(lines)


def build_table(result: dict[str, Any]) -> str:
    cases = {case["case_name"]: case for case in result["cases"]}
    lines = [
        "% Generated by scripts/build_latex_inputs.py; do not edit by hand.",
        r"\begin{tabular}{@{}lrrrrl@{}}",
        r"\toprule",
        r"Case & Group $r$ & Critic $r$ & Delta & CI & Reading \\",
        r"\midrule",
    ]
    for case_name in SELECTED_CASES:
        case = cases[case_name]
        row = " & ".join(
            [
                latex_escape(case["case_name"]),
                fmt(case["mean_group_correlation"]),
                fmt(case["mean_critic_correlation"]),
                fmt(case["mean_critic_minus_group_correlation"]),
                rf"$\pm${fmt(case['ci95_critic_minus_group_correlation'])}",
                latex_escape(case["evidence_by_ci95"].replace("_", " ")),
            ]
        )
        lines.append(row + r" \\")
    lines.extend([r"\bottomrule", r"\end{tabular}", ""])
    return "\n".join(lines)


def build_axis_summary_table(result: dict[str, Any]) -> str:
    axes: dict[str, list[dict[str, Any]]] = {}
    for case in result["cases"]:
        axes.setdefault(case["axis"], []).append(case)

    lines = [
        "% Generated by scripts/build_latex_inputs.py; do not edit by hand.",
        r"\begin{tabular}{@{}lrrrrrrr@{}}",
        r"\toprule",
        r"Axis & Cases & Group $r$ & Critic $r$ & Delta & Clear critic & Near tie & Clear group \\",
        r"\midrule",
    ]
    for axis in sorted(axes):
        cases = axes[axis]
        clear_critic = sum(1 for case in cases if case["evidence_by_ci95"] == "critic_clear")
        near_tie = sum(1 for case in cases if case["evidence_by_ci95"] == "near_tie")
        clear_group = sum(1 for case in cases if case["evidence_by_ci95"] == "group_clear")
        row = " & ".join(
            [
                latex_escape(axis),
                str(len(cases)),
                fmt(fmean(case["mean_group_correlation"] for case in cases)),
                fmt(fmean(case["mean_critic_correlation"] for case in cases)),
                fmt(
                    fmean(
                        case["mean_critic_minus_group_correlation"]
                        for case in cases
                    )
                ),
                str(clear_critic),
                str(near_tie),
                str(clear_group),
            ]
        )
        lines.append(row + r" \\")
    lines.extend([r"\bottomrule", r"\end{tabular}", ""])
    return "\n".join(lines)


def build_full_case_table(result: dict[str, Any]) -> str:
    lines = [
        "% Generated by scripts/build_latex_inputs.py; do not edit by hand.",
        r"\begin{longtable}{@{}L{3.35cm}L{1.25cm}c*{6}{r}@{}}",
        r"\caption{Full 20-seed case summary. Delta is critic $r$ minus group $r$.}\label{tab:full-case-summary}\\",
        r"\toprule",
        r"Case & Axis & Read & Group $r$ & Critic $r$ & $\Delta r$ & 95\% CI & Hit & Wait \\",
        r"\midrule",
        r"\endfirsthead",
        r"\caption[]{Full 20-seed case summary, continued.}\\",
        r"\toprule",
        r"Case & Axis & Read & Group $r$ & Critic $r$ & $\Delta r$ & 95\% CI & Hit & Wait \\",
        r"\midrule",
        r"\endhead",
        r"\midrule",
        r"\multicolumn{9}{r}{\footnotesize\emph{Continued on next page}}\\",
        r"\endfoot",
    ]
    for case in result["cases"]:
        row = " & ".join(
            [
                display_case_name(case["case_name"]),
                display_axis_name(case["axis"]),
                display_ci_read(case["evidence_by_ci95"]),
                fmt(case["mean_group_correlation"]),
                fmt(case["mean_critic_correlation"]),
                fmt(case["mean_critic_minus_group_correlation"]),
                rf"$\pm${fmt(case['ci95_critic_minus_group_correlation'])}",
                fmt(case["mean_critic_exact_state_rate"]),
                fmt(case["mean_wait_token_fraction"]),
            ]
        )
        lines.append(row + r" \\")
    lines.extend([r"\bottomrule", r"\end{longtable}", ""])
    return "\n".join(lines)


def build_raw_seed_table(result: dict[str, Any]) -> str:
    lines = [
        "% Generated by scripts/build_latex_inputs.py; do not edit by hand.",
        r"\begin{longtable}{@{}L{3.55cm}rrrrrrr@{}}",
        r"\caption{Raw case-by-seed estimator-fidelity rows from the deep matrix.}\label{tab:raw-seed-rows}\\",
        r"\toprule",
        r"Case & Seed & Group $r$ & Critic $r$ & $\Delta r$ & Hit & Success & Wait \\",
        r"\midrule",
        r"\endfirsthead",
        r"\caption[]{Raw case-by-seed estimator-fidelity rows, continued.}\\",
        r"\toprule",
        r"Case & Seed & Group $r$ & Critic $r$ & $\Delta r$ & Hit & Success & Wait \\",
        r"\midrule",
        r"\endhead",
        r"\midrule",
        r"\multicolumn{8}{r}{\footnotesize\emph{Continued on next page}}\\",
        r"\endfoot",
    ]
    for case in result["cases"]:
        for row_data in case["seed_results"]:
            row = " & ".join(
                [
                    display_case_name(case["case_name"]),
                    str(row_data["seed"]),
                    fmt(row_data["group_correlation"]),
                    fmt(row_data["critic_correlation"]),
                    fmt(row_data["critic_minus_group_correlation"]),
                    fmt(row_data["critic_exact_state_rate"]),
                    fmt(row_data["success_rate"]),
                    fmt(row_data["wait_token_fraction"]),
                ]
            )
            lines.append(row + r" \\")
        lines.append(r"\addlinespace[2pt]")
    lines.extend([r"\bottomrule", r"\end{longtable}", ""])
    return "\n".join(lines)


